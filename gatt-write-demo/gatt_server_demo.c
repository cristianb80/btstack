/*
 * @section ATT Write
 *
 * @text The only valid ATT write in this example is to the Client Characteristic Configuration, which configures notification
 * and indication. If the ATT handle matches the client configuration handle, the new configuration value is stored.
 * If notifications get enabled, an ATT_EVENT_CAN_SEND_NOW is requested. See Listing attWrite.
 */

/* LISTING_START(attWrite): ATT Write */
static const char * transaction_mode_names[] =
{
    "ATT_TRANSACTION_MODE_NONE    ",
    "ATT_TRANSACTION_MODE_ACTIVE  ",
    "ATT_TRANSACTION_MODE_EXECUTE ",
    "ATT_TRANSACTION_MODE_CANCEL  ",
    "ATT_TRANSACTION_MODE_VALIDATE",
};

static int att_write_callback(hci_con_handle_t con_handle, uint16_t att_handle, uint16_t transaction_mode, uint16_t offset, uint8_t *buffer, uint16_t buffer_size){
    UNUSED(offset);
    UNUSED(con_handle);
    UNUSED(buffer);

    if (buffer){
        // hack to print text buffer
        buffer[buffer_size] = 0;
        printf("Write Callback: att_handle %04x, transaction mode %s text: '%s'\n", att_handle, transaction_mode_names[transaction_mode], buffer);
    } else {
        printf("Write Callback: att_handle %04x, transaction mode %s\n", att_handle, transaction_mode_names[transaction_mode]);
    }

    switch(att_handle){
        case ATT_CHARACTERISTIC_0000FF11_0000_1000_8000_00805F9B34FB_01_VALUE_HANDLE:
        case ATT_CHARACTERISTIC_0000FF12_0000_1000_8000_00805F9B34FB_01_VALUE_HANDLE:
        case ATT_CHARACTERISTIC_0000FF13_0000_1000_8000_00805F9B34FB_01_VALUE_HANDLE:
        case ATT_CHARACTERISTIC_0000FF14_0000_1000_8000_00805F9B34FB_01_VALUE_HANDLE:
            break;
        default:
            break;
    }
    return 0;
}
/* LISTING_END */

